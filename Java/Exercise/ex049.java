public class ex049 {
    public static void main(String[] args) {
        // 런타임 디버깅 사용법
        // 실행 > 디버그 (Shift + F9) 기능 이용
        // Break Point(CTRL + F8) : 중단점을 설정
        // 스텝오버(F8) : 다음 줄로 이동
        // 스텝인투(F7) : 함수 안으로 이동
        // 다시시작(F9) : 계속해서 진행(Resume)
        
        for(int i=0; i<5; i++){
            System.out.println(i);
        }
    }
}
