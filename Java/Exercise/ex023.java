public class ex023 {
    public static void main(String[] args) {
        // 비교 연산자 : < > == != <= >=
        int i = 10;
        int j = 20;

        System.out.println(i == j);
        System.out.println(i != j);
        System.out.println(i < j);
        System.out.println(i > j);
        System.out.println(i <= j);
        System.out.println(i >= j);

    }
}
